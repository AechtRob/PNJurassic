		public WorldGenAbstractTree getRandomTreeFeature(Random rand)
		{
			if (rand.nextInt(22) == 0) {
				return DICKSONIA_TREE;
			}
			int selector = rand.nextInt(13);
			switch (selector) {
				case 0:
					if (Math.random() > 0.4) {
						return TALL_TREE;
					}
					if (Math.random() > 0.8) {
						return REDWOOD_TREE;
					}
					else {
						if (Math.random() > 0.75) {
							return PAGIOPHYLLUM_TREE;
						}
						else {
							return CUNNINGHAMIA_TREE;
						}
					}
				case 1:
					if (Math.random() > 0.8) {
						return HOOP_TREE;
					}
					return ELATOCLADUS_TREE;
				case 2:
					return ELATOCLADUS_TREE;
				case 3:
					if (Math.random() > 0.8) {
						return MONKEYPUZZLE_TREE;
					}
					else {
						return CUNNINGHAMIA_TREE;
					}
				case 4:
					return NULL_TREE;
				case 5:
					return MIROVIA_TREE;
				case 6:
					return CEPHALOTAXUS_TREE;
				case 7:
					return CEPHALOTAXUS_TREE;
				case 8:
					return CEPHALOTAXUS_TREE;
				case 9:
					if (Math.random() > 0.7) {
						return HOOP_TREE;
					}
					return CUNNINGHAMIA_TREE;
				case 10:
					if (Math.random() > 0.5) {
						return BRACHYPHYLLUM_TREE;
					}
					else {
						return HIRMERIELLA_TREE;
					}
				case 11:
					return CYPRESS_TREE;
				case 12:
					return DESMIOPHYLLUM_TREE;
			}
			return ELATOCLADUS_TREE;
		}

